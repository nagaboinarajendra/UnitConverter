package org.java.maven.unitconverter;

public class ConvertToMeters {


	public static void inchesToMeters(double inches) {
		
		System.out.println(inches + " Inches is equal to "+ (inches*0.0254) +" Meters");
	}

	public static void feetToMeters(double feets) {
		
		System.out.println(feets + " Feets is equal to "+ (feets*0.3048) +" Meters");
		
	}
	

}
