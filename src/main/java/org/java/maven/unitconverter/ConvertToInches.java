package org.java.maven.unitconverter;

public class ConvertToInches {

	public static void metersToInches(double meters) {
		
		System.out.println(meters + " Meters is equal to "+ (meters*39.3701) +" Inches");

		
	}

	public static void feetToInches(double feets) {
		
		System.out.println(feets + " Feets is equal to "+ (feets*12.00000648) +" Inches");

		
	}

}
