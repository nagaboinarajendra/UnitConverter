package org.java.maven.unitconverter;

public class ConvertToFeets {

	public static void metersToFeets(double meters) {
		
		System.out.println(meters + " Meters is equal to "+ (meters*3.280841666667) +" Feets");

		
	}

	public static void inchesToFeet(double inches) {
		
		System.out.println(inches + " Inches is equal to "+ (inches*0.08333337833334178435) +" Feets");

		
	}

}
